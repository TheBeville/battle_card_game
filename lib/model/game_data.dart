class GameData {
  final int playerScore;
  final int opponentScore;
  final int opponentCloseScore;
  final int opponentRangedScore;
  final int opponentSiegeScore;
  final int playerCloseScore;
  final int playerRangedScore;
  final int playerSiegeScore;

  const GameData({
    required this.playerScore,
    required this.opponentScore,
    required this.opponentCloseScore,
    required this.opponentRangedScore,
    required this.opponentSiegeScore,
    required this.playerCloseScore,
    required this.playerRangedScore,
    required this.playerSiegeScore,
  });
}
